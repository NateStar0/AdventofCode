var work = input.split("\n");
var tally = 0;
var width = work[0].length;
var height = work.length;
var rot = 0;
var init = true;
var prev = [];

for(var u = 0; u < work.length; u++)
{
	prev[u] = work[u];
}

function count(main_str, sub_str) 
{
    main_str += '';
    sub_str += '';

    if (sub_str.length <= 0) 
    {
        return main_str.length + 1;
    }

       subStr = sub_str.replace(/[.*+?^${}()|[\]\\]/g, '\\$&');
       return (main_str.match(new RegExp(subStr, 'gi')) || []).length;
}

function getType(x, y, a)
{
	var line = a[y];
	if((x < 0) || (x >= width) || (y < 0) || (y >= work.length)) return ".";
	return line.charAt(x);
}

String.prototype.replaceAt = function(index, replacement) {
    return this.substr(0, index) + replacement + this.substr(index + replacement.length);
}

while(!nextIsSame(work, prev) || init)
{
	init = false;
	
	for(var q = 0; q < work.length; q++) 
	{
		prev[q] = work[q];
	}
	
	work = cycle(work);
	rot += 1;
}

