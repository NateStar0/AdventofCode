function nextIsSame(arr, prev)
{
	for(var t = 0; t < arr.length; t++)
	{
		for(var z = 0; z < arr[t].length; z++)
		{
			if(arr[t].charAt(z) != prev[t].charAt(z)) return false;
		}
	}
	
	return true;
}

function cycle(arr)
{
	var a = []; //Copy of arr
	
	for(var o = 0; o < arr.length; o++) 
	{
		a[o] = arr[o];
	}
	
	for(var k = 0; k < height; k++)
	{
		for(var j = 0; j < width; j++)
		{
			var chr = arr[k].charAt(j);
			var neigh = ""
			
			neigh += getType(j - 1, k - 1, arr)	//Diag top left
			neigh += getType(j + 1, k - 1, arr)	//Diag top right
			neigh += getType(j - 1, k + 1, arr)	//Diag bottom left
			neigh += getType(j + 1, k + 1, arr)	//Diag bottom right
				
			neigh += getType(j, k - 1, arr)	//Top
			neigh += getType(j, k + 1, arr)	//Bottom
			neigh += getType(j - 1, k, arr)	//Left
			neigh += getType(j + 1, k, arr)	//right

			//console.log(neigh, k, j)
			if(!neigh.includes("#") && chr == "L") 
			{
				a[k] = a[k].replaceAt(j, "#")
			}
			
			if(count(neigh, "#") >= 4 && chr == "#") 
			{
				a[k] = a[k].replaceAt(j, "L")
			}
			
		}
	}
	
	//console.log(a, arr);
	return a;
}

